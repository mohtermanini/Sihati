    {
        $this->validate($request,[
            'content' => 'required',
            'consultation_id' => 'required|int'
        ]);
        Comment::create([
            'content' => $request->content,
            'user_id' => Auth::id(),
            'consultation_id' => $request->consultation_id
        ]);
        Session::flash("success","تم إضافة التعليق بنجاح");
        return redirect()->back();
    }

